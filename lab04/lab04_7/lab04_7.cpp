#include <iostream>
#include <cmath>

using namespace std;

bool isPrime(int n) {
    // 1. ตรวจสอบเงื่อนไขพื้นฐาน
    if (n <= 1) {
        // จำนวนเฉพาะต้องมากกว่า 1
        return false;
    }

    if (n <= 3) {
        // 2 และ 3 เป็นจำนวนเฉพาะ
        return true;
    }

    // 2. ตรวจสอบการหารด้วย 2 และ 3
    if (n % 2 == 0 || n % 3 == 0) {
        // ถ้าหารด้วย 2 หรือ 3 ลงตัว ถือว่าไม่ใช่จำนวนเฉพาะ
        return false;
    }

    // 3. ใช้วิธีการตรวจสอบการหารที่ดีที่สุด (Optimized Check)
    // เราเพียงแค่ต้องตรวจสอบตัวหารไปจนถึงรากที่สองของ n เท่านั้น
    // และใช้ก้าวเดิน (step) 6 เพื่อข้ามตัวเลขที่ไม่จำเป็น (เพราะจำนวนเฉพาะที่มากกว่า 3
    // จะอยู่ในรูปแบบ 6k + 1 หรือ 6k - 1)

    // i เริ่มต้นที่ 5
    for (int i = 5; i * i <= n; i = i + 6) {
        // i * i <= n มีค่าเท่ากับ i <= sqrt(n)

        // ตรวจสอบตัวหารในรูปแบบ 6k - 1
        if (n % i == 0) {
            return false;
        }

        // ตรวจสอบตัวหารในรูปแบบ 6k + 1
        if (n % (i + 2) == 0) {
            return false;
        }
    }

    // หากผ่านการตรวจสอบทั้งหมด แสดงว่าเป็นจำนวนเฉพาะ
    return true;
}

